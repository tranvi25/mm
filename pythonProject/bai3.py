# thư viên toán học math
#math.ceil(x) trả vê giá trị trần của x,số nguyên nhỏ nhất lớn hơn hoặc bằng x
#math.fabs(x) trả về giá trị tuệt đối của x
#math.float(x) trả về sàn của x ,số nguyên lớn nhất nhỏ hơn hoặc bằng x
#math.exp(x) trả về e lũy từa x,trong đó e=2,718281.. là cơ số của logarit tự nhiên.
#math.log(x[bare]) trả về logarit tự nhiên của x(cơ số e).
#math.pow(x,y) trả về x lũy thừa y
import math

x = float(input("x: "))
print("pi = ", math.pi)
print("|x| = ", math.fabs(x))
print("sqrt(x) = ", math.sqrt(x))
print("ceil(x) = ", math.ceil(x))
print("floor(x) = ", math.floor(x))

